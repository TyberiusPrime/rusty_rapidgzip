#[test]
fn rle_distance_one() {
    // 1000 identical bytes -> a single literal then a long distance-1 back-ref,
    // exercising the 8-byte broadcast store that over-writes past `dst + length`.
    assert_eq!(inflate_all(RLE_DEFLATE), vec![b'x'; 1000]);
}

#[test]
fn overlapping_copy() {
    // Period-3 data -> matches with distance 3 < length, the byte-at-a-time
    // overlapping branch of `copy_back_raw`.
    let expected: Vec<u8> = b"abc".iter().copied().cycle().take(2100).collect();
    assert_eq!(inflate_all(OVERLAP_DEFLATE), expected);
}

#[test]
fn nonoverlapping_chunked_copy() {
    // A 64-byte distinct block repeated -> distance 64 >= length matches, driving
    // `copy_chunked_unchecked` and its COPY_N-byte over-copy headroom.
    let block: Vec<u8> = (0u8..64).collect();
    let expected: Vec<u8> = block.iter().copied().cycle().take(64 * 40).collect();
    assert_eq!(inflate_all(NONOVERLAP_DEFLATE), expected);
}

#[test]
fn mixed_literals_and_refs() {
    // Pseudo-random ASCII -> dynamic-Huffman block mixing literals and matches:
    // stresses the `load_le_u64` refill, literal `*out_ptr.add(cur)` writes and
    // the final `out.set_len(cur)`.
    assert_eq!(inflate_all(MIXED_DEFLATE), ascii_payload(3000));
}

/// The u16 dense-window kernel (`copy_back_u16`) must decode byte-identically to
/// the serial fast path. Driven from bit 0 so the whole stream stays in-window
/// (no marker overshoot), which still exercises every unsafe copy in that path.
fn check_u16_matches_serial(deflate: &[u8]) {
    let expected = inflate_all(deflate);

    let mut padded = deflate.to_vec();
    padded.extend_from_slice(&[0u8; 16]);

    let mut chunk = SpeculativeChunk::default();
    let mut scratch: Vec<u16> = Vec::new();
    decode_until_u16(&padded, 0, u64::MAX, &mut chunk, &mut scratch)
        .expect("decode_until_u16 failed");

    assert!(
        chunk.markers.is_empty(),
        "no overshoot markers expected from bit 0"
    );
    assert_eq!(chunk.bytes, expected, "u16 path diverged from serial path");

    // Side-table speculative path over the same input, for good measure.
    let mut side = SpeculativeChunk::default();
    decode_member(&padded, 0, &mut side).expect("decode_member failed");
    assert_eq!(
        side.bytes, expected,
        "side-table path diverged from serial path"
    );
}

#[test]
fn u16_overlap() {
    check_u16_matches_serial(OVERLAP_DEFLATE);
}

#[test]
fn u16_rle() {
    check_u16_matches_serial(RLE_DEFLATE);
}

// ── speculative marker resolution (`resolve_markers`) ───────────────────────
//
// The serial paths above decode from bit 0 and never overshoot, so they never
// produce markers — `resolve_markers` (the unsafe core the parallel pipeline
// relies on to patch a speculatively-decoded chunk against the previous
// chunk's tail) was previously UNCOVERED by Miri. These tests drive it
// directly with hand-built markers. `Marker { out_pos, prefix_offset }` maps
// `chunk.bytes[out_pos] = prev_tail[prev_tail.len() - 1 - prefix_offset]`
// (prefix_offset 0 = the byte immediately before the chunk).

#[test]
fn resolve_markers_safe_path() {
    // prev_tail shorter than 32 KiB -> the bounds-checked resolution path.
    let prev_tail = b"ABCDEFGH"; // len 8; offset 0 -> 'H', offset 7 -> 'A'
    let mut chunk = SpeculativeChunk::default();
    chunk.bytes = vec![0u8; 3];
    chunk.markers = vec![
        Marker {
            out_pos: 0,
            prefix_offset: 0,
        }, // -> 'H'
        Marker {
            out_pos: 1,
            prefix_offset: 7,
        }, // -> 'A'
        Marker {
            out_pos: 2,
            prefix_offset: 3,
        }, // -> 'E'
    ];
    resolve_markers(&mut chunk, prev_tail).expect("resolve_markers failed");
    assert_eq!(&chunk.bytes, b"HAE");
}

#[test]
fn resolve_markers_fast_path() {
    // prev_tail >= 32 KiB -> the unchecked raw-pointer fast path (all valid
    // 15-bit prefix_offsets are in range, so the bounds checks are elided).
    let n = 40_000usize;
    let prev_tail: Vec<u8> = (0..n).map(|i| (i % 251) as u8).collect();
    let offsets = [0u16, 1, 1000, 32767];
    let mut chunk = SpeculativeChunk::default();
    chunk.bytes = vec![0u8; offsets.len()];
    chunk.markers = offsets
        .iter()
        .enumerate()
        .map(|(i, &o)| Marker {
            out_pos: i as u32,
            prefix_offset: o,
        })
        .collect();
    resolve_markers(&mut chunk, &prev_tail).expect("resolve_markers failed");
    for (i, &o) in offsets.iter().enumerate() {
        assert_eq!(chunk.bytes[i], prev_tail[n - 1 - o as usize], "marker {i}");
    }
}

#[test]
fn resolve_markers_out_of_range_errors() {
    // prefix_offset at/after the tail length must be rejected, not read OOB.
    let prev_tail = b"abc";
    let mut chunk = SpeculativeChunk::default();
    chunk.bytes = vec![0u8; 1];
    chunk.markers = vec![Marker {
        out_pos: 0,
        prefix_offset: 3,
    }];
    assert!(resolve_markers(&mut chunk, prev_tail).is_err());
}
