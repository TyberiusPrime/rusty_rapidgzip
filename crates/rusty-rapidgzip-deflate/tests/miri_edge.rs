//! Self-contained Miri edge-case coverage for the `fast_inflate` unsafe paths.
//!
//! These tests embed pre-compressed raw-deflate streams as constants so they run
//! under `cargo miri test` WITHOUT spawning `gzip` (Miri cannot exec subprocesses,
//! which is why the in-crate `#[cfg(test)] mod tests` — all of which shell out to
//! `gzip` — are unusable under Miri).
//!
//! Each stream is crafted to drive a specific unsafe back-reference kernel:
//!   * RLE        — `copy_back_raw` distance==1 (8-byte broadcast over-store)
//!   * OVERLAP    — `copy_back_raw` distance < length (overlapping byte copy)
//!   * NONOVERLAP — `copy_chunked_unchecked` distance >= length + COPY_N over-copy headroom
//!   * MIXED      — `load_le_u64` bit refill + literal `*out_ptr.add(cur)` writes + `set_len`
//!
//! The u16 dense-window path (`decode_until_u16` -> `copy_back_u16`) is covered too.
//!
//! Run: `cargo +nightly miri test -p rusty-rapidgzip-deflate --test miri_edge`

use rusty_rapidgzip_deflate::fast_inflate::{decode_member, decode_until_u16, inflate_fast};
use rusty_rapidgzip_deflate::{resolve_markers, BitReader, Marker, SpeculativeChunk};

/// Decode a raw-deflate stream via the serial fast path. Pads the input with the
/// over-read/over-write headroom `inflate_fast` relies on (mirrors the crate's
/// own `check_inflate_fast` helper).
fn inflate_all(deflate: &[u8]) -> Vec<u8> {
    let mut padded = deflate.to_vec();
    padded.extend_from_slice(&[0u8; 16]);
    let mut br = BitReader::new(&padded);
    let mut out = Vec::new();
    inflate_fast(&mut br, &mut out).expect("inflate_fast failed");
    out
}

fn ascii_payload(n: usize) -> Vec<u8> {
    let mut s: u64 = 0x9E37_79B9_7F4A_7C15;
    let mut p = Vec::with_capacity(n);
    while p.len() < n {
        s = s
            .wrapping_mul(6364136223846793005)
            .wrapping_add(1442695040888963407);
        p.push(((s >> 56) as u8 % 95) + 32);
    }
    p
}

